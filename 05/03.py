# Dla podanej przez użytkownika liście liczb całkowitych sprawdź czy pierwszy i ostatni element są takie same.

user_num = [3, 6, 10, 32, 9, 22, 2, 982, 43, 13]

result = True if user_num[0] == user_num[-1] else False

print("Pierwsza i ostatnia wartość są takie same: ", result)

print("Pierwsza i ostatnia wartość są takie same: ", user_num[0] == user_num[-1])